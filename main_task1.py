def get_all_mentors(mentors):
    all_list = [mentor for mentors_list in mentors for mentor in mentors_list]
    return all_list

def extract_names(all_list):
    all_names_list = [mentor.split(' ')[0] for mentor in all_list]
    return all_names_list

def get_unique_names(all_names_list):
    unique_names = set(all_names_list)
    return unique_names

def sort_and_join_names(unique_names):
    all_names_sorted = sorted(unique_names)
